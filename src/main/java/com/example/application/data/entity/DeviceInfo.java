package com.example.application.data.entity;

public class DeviceInfo {
   String current_time;
   String firmware_version;
   String device_name;
   String serial_number;
   String mac_address;
   String face_version;
   String finger_algorithm;
   String platform_information;
   String ip;
   String netmask;
   String geteway;


    public String getCurrent_time() {
        return current_time;
    }

    public void setCurrent_time(String current_time) {
        this.current_time = current_time;
    }

    public String getFirmware_version() {
        return firmware_version;
    }

    public void setFirmware_version(String firmware_version) {
        this.firmware_version = firmware_version;
    }

    public String getDevice_name() {
        return device_name;
    }

    public void setDevice_name(String device_name) {
        this.device_name = device_name;
    }

    public String getSerial_number() {
        return serial_number;
    }

    public void setSerial_number(String serial_number) {
        this.serial_number = serial_number;
    }

    public String getMac_address() {
        return mac_address;
    }

    public void setMac_address(String mac_address) {
        this.mac_address = mac_address;
    }

    public String getFace_version() {
        return face_version;
    }

    public void setFace_version(String face_version) {
        this.face_version = face_version;
    }

    public String getFinger_algorithm() {
        return finger_algorithm;
    }

    public void setFinger_algorithm(String finger_algorithm) {
        this.finger_algorithm = finger_algorithm;
    }

    public String getPlatform_information() {
        return platform_information;
    }

    public void setPlatform_information(String platform_information) {
        this.platform_information = platform_information;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getNetmask() {
        return netmask;
    }

    public void setNetmask(String netmask) {
        this.netmask = netmask;
    }

    public String getGeteway() {
        return geteway;
    }

    public void setGeteway(String geteway) {
        this.geteway = geteway;
    }
}
